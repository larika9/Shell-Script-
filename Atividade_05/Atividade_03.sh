#!/bin/bash

echo "3º - Escreva um script que calcule (e exiba na tela) a soma de todos os números de 1 até 20. Melhore este script para que peça para o usuário digitar dois números, a e b, e calcule a soma dos números de a até b."

#part.1º#
for i in $(seq 1 20)
do
    a = ${i}
    b = $((0 + $a))
done
echo "$b"

